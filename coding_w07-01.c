#include <stdio.h>

int main() {
    float score;              // ตัวแปรเก็บคะแนนสอบ
    float finalScore;         // ตัวแปรเก็บคะแนนรวมหลังตรวจเงื่อนไข

    // ─────────────────────────────
    // Input: รับค่าคะแนนสอบจากผู้ใช้
    // ─────────────────────────────
    printf("Enter midterm score: ");
    scanf("%f", &score);

    // ─────────────────────────────
    // Process: ตรวจสอบเงื่อนไข ถ้า >= 50 ให้บวกเพิ่มอีก 5%
    // ─────────────────────────────
    if (score >= 50) {
        finalScore = score + (score * 0.05);      // เพิ่ม 5%
    } else {
        finalScore = score;                       // ไม่เพิ่มคะแนน
    }

    // ─────────────────────────────
    // Output: แสดงคะแนนรวมหลังจากเพิ่ม (ถ้ามี)
    // ─────────────────────────────
    printf("Final Score: %.2f\n", finalScore);

    // พิมพ์ข้อความสิ้นสุดการทำงาน
    printf("End of evaluation\n");

    return 0;         // สิ้นสุดโปรแกรม
}